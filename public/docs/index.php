        <h3>Честный знак</h3>
        <p>
            Проверка товаров по маркировке Честный знак доступна в документах отгрузки.
        </p>
        <p>
            Документе отгрузки в строке с товаров должен быть указан знак "М" у маркируемых товаров.
        </p>

        <img src="images/znak.png" />

        <p>
            Вы можете указать маркировку вручную, если знаете точный код маркировки через стандартный интерфейс ERP-системы МойСклад.
        </p>

        <img src="images/mm.png" />

        <p>
            Для работы с маркировкой по правилам ERP-системы МойСклад, изучите <a href="https://support.moysklad.ru/hc/ru/sections/360004216813">справочный материал "Маркировка товаров"</a>
        </p>
        <p>
            Все сведения о маркировке содержатся в разделе "Товары. Маркировка" (данный функционал не входит в состав модуля, это стандартный функционал ERP-системы МойСклад).
        </p>

